        String x = camp_cpf.getText();
        String opc = "opc invalida";
        
        if(radio_cep.isSelected()){
            opc = "cep";
        }else if(radio_cpf.isSelected()){
            opc = "cpf";
        }
        
        boolean x1 = TesteRegex(x, opc);

        if(x1){
            resultado.setForeground(Color.blue);
            resultado.setText("O "+ opc + " " + x +" e valido");
        }
        else{
            resultado.setForeground(Color.red);
            if(opc.equals("opc invalida")){
                resultado.setText("Selecione uma das op√ßoes acima");
            }else{
                resultado.setText("O "+ opc+ " " + x +" e invalido");
            }
        }

