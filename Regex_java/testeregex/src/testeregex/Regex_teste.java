    private boolean TesteRegex(String palavra, String opc){
        String regex = "";

        if("cpf".equals(opc)){
            regex = "^\\d{3,3}\\.\\d{3,3}\\.\\d{3,3}\\-\\d{2,2}$";
        }
        if("cep".equals(opc)){
            regex = "^\\d{5,5}\\-\\d{3,3}$";
        }
        
        if("".equals(regex)){
            return false;
        }
        
        Pattern pattern =  Pattern.compile(regex);
        Matcher matcher = pattern.matcher(palavra);
        boolean matchfound = matcher.find();
        
        if(matchfound){
            return true;
        }
        else{
            return false;
        }
    }
